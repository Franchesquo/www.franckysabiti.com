   <p class="lead-text">
<strong>1. Les composants de l'identité de marque</strong> <br> 

Logo : L’élément visuel le plus reconnaissable, souvent le symbole principal.<br> 

Palette de couleurs : Les couleurs qui évoquent la marque et créent la cohérence visuelle.<br> 

Typographie : Les polices de caractères utilisées pour communiquer uniformément.<br> 

Slogan ou tagline : Une phrase courte qui résume l’essence ou la promesse de la marque.<br> 

Iconographie et graphismes : Icônes, illustrations ou motifs qui renforcent la reconnaissance.<br> 

Ton et style de communication : La manière dont la marque s’exprime (formel, chaleureux, humoristique…).
<br><br>

<strong>2. Pourquoi le brand identity est important</strong> <br>

Reconnaissance immédiate : Une identité forte permet aux consommateurs d’identifier la marque rapidement.<br> 

Différenciation : Elle aide à se distinguer des concurrents.<br> 

Cohérence : Assure que tous les supports (site web, publicité, réseaux sociaux, emballages) reflètent la même image.
<br> 
Fidélisation : Une identité claire et mémorable crée de la confiance et renforce la loyauté des clients.
<br><br>

<strong>3. Exemples concrets</strong> <br>

Apple : Minimaliste, moderne, innovant. Logo simple et couleurs sobres.<br> 

Coca-Cola : Rouge vif, typographie classique, message émotionnel lié au bonheur.<br> 

Nike : Swoosh simple, slogan “Just Do It”, identité dynamique et motivante.<br> <br> 

<strong>4. Différence entre Brand Identity et Brand Image

Brand Identity (identité de marque) :</strong> <br> 
 Ce que l’entreprise veut montrer au public.<br> 

Brand Image (image de marque) : Ce que le public perçoit réellement.
</p>
