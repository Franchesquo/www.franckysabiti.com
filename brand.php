                <h2>Le Brand Identity, ou identité de marque</h2>
              </div>
              <p class="lead-text">est un concept fondamental en marketing et communication. Il s’agit de l’ensemble des éléments visuels, verbaux et émotionnels qui définissent et représentent une marque auprès de son public. L’identité de marque va au-delà du simple logo : elle traduit la personnalité, les valeurs et la promesse d’une entreprise. Voici les points essentiels: </p>

              <div class="service-image-showcase" data-aos="zoom-in" data-aos-delay="300">
              <img src="assets/img/services/fcs.PNG" alt="Brand" class="img-fluid main-image">
